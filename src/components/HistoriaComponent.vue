        <p>La historia no termina ahí, puesto que con miras al futuro surge la idea y la necesidad de crear un bachillerato. Es por eso que se consigue mediante resolución ministerial N°. 443 del 31 de julio de 1975, con su primera especialidad, Comercio y Administración especialidad Contabilidad; con resolución 1377 del 7 de agosto de 1979 se creó la especialidad de Físico Matemático y Químico Biológicas, posteriormente mediante resolución 1573 de mayo de 1982 se inicia la especialidad de ciencias sociales, Resolución 118 del 18 de febrero de 1988 se creó la especialidad de Físico Matemático (lo que originó la separación de las especialidades de Químico y Físico) en la sección diurna. Debido con la gran demanda de personas que no podían acceder a la jornada matutina se inicia las gestiones para obtener la sección Nocturna y mediante resolución 1272 del 15 de junio de 1972, se autoriza su funcionamiento a partir del 1972 – 1973 por parte del Ministerio de Educación.</p>
        <p>Desde aquel tiempo el colegio El Carmen, ha experimentado cambios sustanciales en todos sus procesos, por él han desfilado rectores, vicerrectores, inspectores generales, y docentes con alto espíritu de pertinencia, y cada uno de ellos ha dejado un legado en beneficio de esta institución.</p>
        <p>El tiempo siguió su curso y con el desafío a nuevas propuestas y cambios educativos; a partir del año 2010, se inició con las gestiones administrativas para la obtención del Programa de Diploma de Bachillerato Internacional, consiguiéndose la carta de aceptación en diciembre del 2014. Por disposición ministerial siendo Ministro de Educación el Dr. Augusto Espinoza y Director del Distrito de Educación de El Carmen, Ing. Jorge Mero Oña, se iniciaron las fusiones de instituciones educativas; “Santa Marianita de Jesús” en Mayo del 2015 siendo la rectora la Mg. Meri Noemí Cañar Meléndez, desde entonces el Colegio Nacional “El Carmen”, cambio su denominación a UNIDAD EDUCATIVA “EL CARMEN”. En el mismo año se integró parte de la Escuela “Aida León de Rodríguez Lara”. En el 2016, se fusiona en su totalidad las Escuelas “Aida León de Rodríguez Lara” y “Dr. José Ricardo Martínez Cobo”, estando como rectora desde el mes de mayo, la Mg. Teresa Elizabeth Ramírez Cevallos.</p>
        <p>En la actualidad la Unidad Educativa “El Carmen” oferta los tres niveles de Educación: Inicial, Educación General Básica y Bachillerato en Ciencias y Técnico en Contabilidad y el Programa de Diploma de Bachillerato Internacional, funcionando en tres bloques, con una población estudiantil de 2906 estudiantes, 126 docentes 5 personal administrativo y tres de servicio.</p>
