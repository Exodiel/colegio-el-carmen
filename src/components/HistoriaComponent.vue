        <p itemprop="description">La historia del Colegio Nacional El Carmen, empieza en 1970, cuando un grupo de personas visionarias vieron la necesidad de que en nuestro cantón era necesario una institución educativa fiscal que ofreciera una educación a nivel secundaria, ya que para ese entonces ya existía el Colegio Particular Nocturno “Patria Nueva”, pero que por su sostenimiento, muchos jóvenes sedientos de saber, no podrían acceder a este tipo de educación. Se inician las gestiones con la idea de fiscalizar al Colegio Patria Nueva, suscrito por el Sr. Artemio Avellán Vera Jefe Político de nuestro cantón. Su constancia en las gestiones permitió que se fiscalice no el Colegio Patria Nueva sino una nueva Institución con la razón social de Colegio de Ciclo Básico Nacional El Carmen, mediante decreto supremo 190 del 4 de agosto de 1970, publicado en el registro oficial número 36 del 12 de agosto de 1970, siendo presidente de la República el Dr. José María Velazco Ibarra. Por no contar con un local propio para dar inicio a sus labores educativas, el Sr. Marco Aurelio Angulo Basurto presta su casa ubicada en el barrio central del cantón, donde da sus primeros pasos como institución educativa el 17 de noviembre de 1970, empezando a funcionar con ocho maestros, entre ellos: Un rector - profesor, Sr. Segundo Víctor Vargas Tamariz, el Sr. Felipe Santiago Sucre Rodríguez Angulo Secretario Colector y el Sr. José Pedro Gilces Conserje, los profesores: Egdo. Colón Esperidion Ortega Mendieta, Sr. Medardo Ángel Párraga Intriago, Sra. Flor Alejandrina González O. de Vásconez, Srta. Oliva Dalia Champutiz Mancero y la Srta. Ligia Beatriz Aurora Gonzáles Dávila, con 40 estudiantes; 22 mujeres y 18 hombres.</p>
